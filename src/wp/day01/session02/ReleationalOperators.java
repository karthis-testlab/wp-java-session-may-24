package wp.day01.session02;

public class ReleationalOperators {

	public static void main(String[] args) {
		
		int x = 10;
		int y = 5;
		
		System.out.println(x > y);
		
		System.out.println(x < y);
		
		System.out.println(x >= y);
		
		System.out.println(x <= y);
		
		System.out.println(x == y);
		
		System.out.println(x != y);

	}

}
