package wp.day01.session02;

public class ArithmeticOperators {

	public static void main(String[] args) {
		
		// +, -, *, /, %
		
		int a = 5;
		int b = 5;
		
		// b++ ==> b = b+1
		
		System.out.println(a + b);
		
		System.out.println(a - b);
		
		System.out.println(a * b);
		
		System.out.println(a / b);
		
		System.out.println(a % b);

	}

}
