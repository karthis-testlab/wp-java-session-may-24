package wp.week03.session01;

public class WhileLoopPractice {

	public static void main(String[] args) {
		
		// Write the code to print numbers from 1 to 10;
		
		// for (int i = 1; i <= 10; i++)
		
		int a = 1, b = 10;
		
		while(a <= b) {
			System.out.println(a);
			a++;
		}
		
		System.out.println("While loop ends.");
		

	}

}
