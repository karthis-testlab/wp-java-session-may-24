package wp.week01.session01;

public class NonNumericPrimitiveDataType {

	public static void main(String[] args) {
		
		char name = 'a';
		System.out.println(name);
		
		char name1 = '6';
		System.out.println(name1);
		
		char name2 = '\u0051';
		System.out.println(name2);
		
		boolean value = true;
		System.out.println(value);

	}

}
